package com.aurionpro;

public class ArithmeticOperators {
	public static void main(String[] args) {
		int num1 = 45;
		int num2 = 16;

		int sum = num1 + num2;

		System.out.println("The sum of " + num1 + " and " + num2 + " is " + sum);

		int prod = num1 * num2;
		int subt = num1 - num2;
		int div = num1 / num2;

		System.out.println("The product of " + num1 + " and " + num2 + " is " + prod);
		System.out.println("The subtraction of " + num1 + " by " + num2 + " gives " + subt);
		System.out.println("The division of " + num1 + " by " + num2 + " gives " + div);
	}
}
